#if defined(LIBUSB_API_VERSION)
int callback_device_hotplugged(libusb_context *ctx, libusb_device *dev,
                               libusb_hotplug_event ev, void *u) {
  struct libusb_device_descriptor desc;
  volatile int *ready = (int *)u;
  int result;

  result = libusb_get_device_descriptor(dev, &desc);
  if (result < 0) {
    return 0;
  }

  if (desc.idProduct == AR2300_PRODUCT_ID &&
      desc.idVendor == AR2300_VENDOR_ID) {
    switch (ev) {
      case LIBUSB_HOTPLUG_EVENT_DEVICE_ARRIVED:
        *ready = 1;
        break;
      case LIBUSB_HOTPLUG_EVENT_DEVICE_LEFT:
        *ready = -1;
        break;
      default:
        printf("unknown hotplug event\n");
        break;
    }
  }
  return 0;
}
#endif

libusb_device_handle *write_firmware(libusb_context *ctx,
                                     libusb_device_handle *handle) {
  firmware_packet *p = firmware;
  volatile int ready = 0;
  int has_callback = 0;
#if defined(LIBUSB_API_VERSION)
  libusb_hotplug_callback_handle callback;
#endif
  struct timeval tv;
  int result = 0;

#if defined(LIBUSB_API_VERSION)
  if (LIBUSB_API_VERSION >= 0x01000102 &&
      libusb_has_capability(LIBUSB_CAP_HAS_HOTPLUG)) {
    libusb_hotplug_register_callback(
        ctx,
        LIBUSB_HOTPLUG_EVENT_DEVICE_ARRIVED | LIBUSB_HOTPLUG_EVENT_DEVICE_LEFT,
        LIBUSB_HOTPLUG_NO_FLAGS, AR2300_VENDOR_ID, AR2300_PRODUCT_ID,
        LIBUSB_HOTPLUG_MATCH_ANY, callback_device_hotplugged, (void *)&ready,
        &callback);
    has_callback = 1;
  }
#endif

  tv.tv_sec = 0;
  tv.tv_usec = 10000;

  for(int i = 0; i < FIRMWARE_PACKETS_LENGTH; i++) {
    result = libusb_control_transfer(handle, 0x40, p[i].request, p[i].address, 0,
                                     p[i].data, p[i].length, 5000);
    if (result < 0) {

      goto ERR;
    }
    if (result != p[i].length) {
      result = 1;
      goto ERR;
    }

    result = 0;
    if (p[i].request == 0xa0 && p[i].address == 0xe600 && p[i].data[0] == 0x00) {
      /* check if machine resets */
      /* machine was reset - reacquire handle */
      libusb_release_interface(handle, AR2300_IF_NO);
      libusb_close(handle);

      /* during open, there is should be no thread running */

      if (has_callback) {
        int idx;

        for (idx = 0; ready <= 0 && idx < 300; ++idx) {
          libusb_handle_events_timeout_completed(ctx, &tv, NULL);
          if (ready < 0) {
            idx = 0;
          }
        }
        ready = 0;
      } else {
        sleep(3);
      }

      handle = open_device(ctx);
      if (!handle) {
        goto ERR;
      }
      result = libusb_claim_interface(handle, AR2300_IF_NO);
      if (result < 0) {
        goto ERR;
      }
    }
  }
ERR:
  if (result != 0 && handle) {
    libusb_close(handle);
    handle = NULL;
  }
#if defined(LIBUSB_API_VERSION)
  if (has_callback) {
    libusb_hotplug_deregister_callback(ctx, callback);
  }
#endif
  return handle;
}
